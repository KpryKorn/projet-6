CREATE TABLE USERS (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(100) NOT NULL UNIQUE,
    email VARCHAR(255) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE SUBJECTS (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(150) NOT NULL,
    description TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE POSTS (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(255) NOT NULL,
    content TEXT NOT NULL,
    author_id BIGINT NOT NULL,
    subject_id BIGINT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    
    CONSTRAINT fk_posts_author FOREIGN KEY (author_id) REFERENCES USERS(id),
    CONSTRAINT fk_posts_subject FOREIGN KEY (subject_id) REFERENCES SUBJECTS(id)
);

CREATE TABLE COMMENTS (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    content TEXT NOT NULL,
    author_id BIGINT NOT NULL,
    post_id BIGINT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_comments_author FOREIGN KEY (author_id) REFERENCES USERS(id),
    CONSTRAINT fk_comments_post FOREIGN KEY (post_id) REFERENCES POSTS(id) ON DELETE CASCADE
);

CREATE TABLE SUBSCRIPTIONS (
    user_id BIGINT NOT NULL,
    subject_id BIGINT NOT NULL,
    
    PRIMARY KEY (user_id, subject_id),
    CONSTRAINT fk_subscriptions_user FOREIGN KEY (user_id) REFERENCES USERS(id) ON DELETE CASCADE,
    CONSTRAINT fk_subscriptions_subject FOREIGN KEY (subject_id) REFERENCES SUBJECTS(id) ON DELETE CASCADE
);

CREATE INDEX idx_posts_author_id ON POSTS(author_id);
CREATE INDEX idx_posts_subject_id ON POSTS(subject_id);
CREATE INDEX idx_comments_author_id ON COMMENTS(author_id);
CREATE INDEX idx_comments_post_id ON COMMENTS(post_id);